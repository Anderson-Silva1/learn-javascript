// Atribuição Simples (=)
let a = 10; // Atribui 10 à variável 'a'
let b = 5; // Atribui 5 à variável 'b'
console.log(a); // 10
console.log(b); // 5

// Atribuição de Soma (+=)
let saldo = 1000;
let deposito = 200;
saldo += deposito; // Equivalente a: saldo = saldo + deposito
console.log(saldo); // 1200

// Atribuição de Subtração (-=)
let contaCorrente = 1500;
let pagamento = 200;
contaCorrente -= pagamento; // Equivalente a: contaCorrente = contaCorrente - pagamento
console.log(contaCorrente); // 1300

// Atribuição de Multiplicação (*=)
let precoProduto = 50;
let quantidade = 3;
precoProduto *= quantidade; // Equivalente a: precoProduto = precoProduto * quantidade
console.log(precoProduto); // 150

// Atribuição de Divisão (/=)
let totalDistancia = 300;
let numeroDeDias = 5;
totalDistancia /= numeroDeDias; // Equivalente a: totalDistancia = totalDistancia / numeroDeDias
console.log(totalDistancia); // 60

// Atribuição de Módulo (%=)
let numero = 17;
numero %= 4; // Equivalente a: numero = numero % 4
console.log(numero); // 1 (resto da divisão de 17 por 4)
